// 获取翻译文本的函数
export function getTranslation(key: string, language: string = defaultLanguage): string {
  const langTranslations = translations[language as keyof typeof translations]
  if (!langTranslations) {
    return translations[defaultLanguage][key as keyof typeof translations['zh-CN']] || key
  }
  return langTranslations[key as keyof typeof langTranslations] || key
}

// 简化的翻译函数
export function t(key: string, language?: string): string {
  return getTranslation(key, language)
}